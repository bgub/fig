import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { htmlElementAttributes } from "html-element-attributes";
import { svgElementAttributes } from "svg-element-attributes";

const target = new URL(
  "../packages/fig-dom/src/jsx-attributes.generated.ts",
  import.meta.url,
);

function literal(value) {
  return JSON.stringify(value);
}

function sorted(values) {
  return [...new Set(values)].sort((left, right) => left.localeCompare(right));
}

function unionType(values) {
  return sorted(values)
    .map((value) => `  | ${literal(value)}`)
    .join("\n");
}

function tagMapType(name, source, globalName) {
  const globals = new Set(source["*"]);
  const tags = sorted(Object.keys(source).filter((tag) => tag !== "*"));
  const lines = [`export interface ${name} {`];

  for (const tag of tags) {
    const own = sorted(source[tag].filter((attr) => !globals.has(attr)));
    const value =
      own.length === 0
        ? globalName
        : `${globalName}\n${own.map((attr) => `    | ${literal(attr)}`).join("\n")}`;
    lines.push(`  ${literal(tag)}: ${value};`);
  }

  lines.push("}");
  return lines.join("\n");
}

const output = `${[
  "// Generated by scripts/generate-jsx-attributes.mjs.",
  "// Do not edit by hand; keep Fig-specific policy in jsx-attribute-policy.ts.",
  "",
  `export type HtmlGlobalAttributeName =\n${unionType(htmlElementAttributes["*"])};`,
  "",
  tagMapType(
    "HtmlAttributeNameByTag",
    htmlElementAttributes,
    "HtmlGlobalAttributeName",
  ),
  "",
  `export type SvgGlobalAttributeName =\n${unionType(svgElementAttributes["*"])};`,
  "",
  tagMapType(
    "SvgAttributeNameByTag",
    svgElementAttributes,
    "SvgGlobalAttributeName",
  ),
  "",
].join("\n")}`;

writeFileSync(target, output);
execFileSync("pnpm", ["exec", "oxfmt", fileURLToPath(target)], {
  stdio: "inherit",
});
execFileSync("pnpm", ["exec", "oxlint", "--fix", fileURLToPath(target)], {
  stdio: "inherit",
});
